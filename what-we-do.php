							<p>70 years on, looking to the next 15.  So it has been 70 years that the United Nations has been operational in the world and this year not only marks the UN’s 70th anniversary, but also marks a new chapter in the development agenda: the launching of the Sustainable Development Goals. These 17 goals will guide development for the next 15 years.</p>
