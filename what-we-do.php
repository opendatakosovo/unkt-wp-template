							<p>On Saturday, 24 October, UN agencies, ambassadors, ministers, the Prime Minister, Deputy Prime Ministers, members of the Parliament, other representatives of local and international institutions, the newly appointed Special Representative to the Secretary General, civil society organisations, and others joined together to celebrate the launching of these new goals in Pristina this past Saturday.</p>
							<p>These 17 Global Goals emerged through intense discussions between several layers of society across the world including in Kosovo on what people wanted to see as the direction for development in the post-2015 era. The consultations were just what this agenda strives for- a way to understand what people need and how this can be achieved. These messages were also passed to local institutions for planning purposes and to integrate these voices with the other1,7 million people’s voices from around the world.</p>
							<div class="video-container">
								<iframe src="http://www.youtube.com/embed/X9Adh9no83M" allowfullscreen="" frameborder="0"></iframe>
							</div>
							<p>So for such a small territory like Kosovo, why are these goals such a big deal? With a population of less than 2 million, Kosovo is often lost in global discussions around development priorities. Kosovo’s active, youthful population didn’t let that happen this time with over 9000 Kosovans having actively participated in the post-2015 debate around setting the development priorities for the coming 15 years.</p>
