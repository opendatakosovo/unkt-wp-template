							<h5>UN Women was established in 2010, merging the mandates of four previously distinct entities [Division for the Advancement of Women (DAW), International Research and Training Institute for the Advancement of Women (INSTRAW), Office of the Special Adviser on Gender Issues and Advancement of Women (OSAGI) and the United Nations Development Fund for Women (UNIFEM)].</h5>
							<div class="section">
								<h3>Mandate</h3>
								<p>The mandate of UN Women is to support inter-governmental bodies, such as the Commission on the Status of Women, in their formulation of policies, global standards and norms, to help Member States to implement these standards, standing ready to provide suitable technical and financial support to those countries that request it, and to forge effective partnerships with civil society and to hold the UN system accountable for its own commitments on gender equality, including regular monitoring of system-wide progress.</p>
							</div>
							<div class="section">
								<h3>Activities</h3>
								<p>In Kosovo since 1999 UNIFEM’s (now UNWOMEN) primary approach has focused on supporting women to claim their rights in the context of post-conflict and rehabilitation processes by the facilitation of partnerships for the capacity building of women’s organizations and government institutions. Since programmes began UN Women fostered women’s leadership for civic participation and local governance, supported the establishment of Kosovo’s gender machinery at both the central and local level.</p>
								<p>Since the adoption of UN Security Council (UNSC) Resolution 1325 (2000), UN Women’s work on peace and security issues has been driven by its goals.</p>
								<p>Between 2012-2014 UN Women in Kosovo supports three projects that promote the implementation of 1325</p>
								<p>a) The UN Women project, Advancing Implementation of UNSCR on Women, Peace and Security in Western Balkans extends the longstanding UN Women engagement to strengthening the women, peace and security agenda throughout the sub-region.   This project is dedicated to the development and strengthening of governmental (security sector focus) and civil society capacities to implement and monitor commitments under UNSCR 1325 (and related Resolutions 1820, 1888, 1889 and 1960) in national laws, policies, strategies and/or plans in Kosovo, Bosnia and Herzegovina, the FYR Macedonia, Albania, and Serbia.</p>
								<p>b) UN Women is part of the UNKT Joint Programme entitled “Support to addressing Gender Based Violence in Kosovo through strengthening the Implementation of the Kosovo Law, National Action Plan and Strategy against Domestic Violence”.  The GBV program is shaped to contribute to the implementation of UNSC Resolution 1325 (2000) on women, peace and security.</p>
								<p>c) UN Women has commenced the implementation of a joint project with UNDP and EU ‘Enhancing Women’s Participation in Peacebuilding and Post-Conflict Planning in Kosovo, Liberia, and Timor-Leste’ which aims to ensure that national and international peacebuilding efforts advance gender equality and address women’s needs and ensure greater participation of women in peace building and post-conflict planning.  This project is financed by EC.</p>
